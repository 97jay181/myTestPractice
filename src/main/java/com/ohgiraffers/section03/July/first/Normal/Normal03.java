package section03.July.first.Normal;

public class Normal03 {

    public static void main(String[] args) {
        // 두 정수 선언
        int num1 = 10;
        int num2 = 20;

        // Math 클래스를 이용하여 큰 수를 찾음
        int max = Math.max(num1, num2);

        // 결과 출력
        System.out.println("두 정수 " + num1 + "과 " + num2 + " 중 큰 수: " + max);

    }
}
